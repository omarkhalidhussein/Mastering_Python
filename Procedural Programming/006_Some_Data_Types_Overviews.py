# ----------------------------
# type()
# All Data in Python is Object
# ----------------------------

print(type(23))  # int => Integer
print(type(34))  # int => Integer
print(type(-4))  # int => Integer

print(type(10.9))  # float => Floating Point Number
print(type(1.42420))  # float => Floating Point Number
print(type(-434.242))  # float => Floating Point Number

print(type("Hello Python"))  # str => String

print(type([9, 8, 7, 1, 6]))  # list => List

print(type((1, 2, 3, 4, 5)))  # tuple => Tuple

print(type({"One": 1, "Two": 2, "Three": 3}))  # dict => Dictionary

print(type(2 == 2))  # bool => Boolean
